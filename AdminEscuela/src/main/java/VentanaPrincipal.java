    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cont = ListaDireccion.getSelectedValue();
        String[] values = cont.split(" ");
        String nombre = values[0];
        String apellido = values[1];

        try {
            String query = "SELECT padres_id,amonestaciones FROM alumno WHERE nombre = ? AND apellido = ?";
            PreparedStatement stmt = conexion.prepareStatement(query);

            stmt.setString(1, nombre);
            stmt.setString(2, apellido);

            ResultSet rs = stmt.executeQuery();

            // Verifica si hay resultados antes de acceder al rs
            if (rs.next()) {
                int idpadres = rs.getInt("padres_id");
                boolean amon = rs.getBoolean("amonestaciones");

                String[] padres = devolverStringPadres(idpadres).split(" ");

                Nombre.setText(padres[0]);
                apellidoo.setText(padres[1]);
                telefono.setText(padres[2]);
                email.setText(padres[3]);

                if (amon) {
                    amonestacion.setText("Sí");
                } else {
                    amonestacion.setText("No");
                }

            } else {
                // Si no hay resultados, muestra un mensaje de error o advertencia
                JOptionPane.showMessageDialog(null, "No se encontraron padres para este alumno.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException ex) {
            Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

