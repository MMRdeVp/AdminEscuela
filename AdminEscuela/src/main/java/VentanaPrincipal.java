    private void consultarUsuario(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_consultarUsuario
        try {
            Statement stmt = conexion.createStatement();

            // Validar las entradas
            String nombre = inputNombre1.getText();
            String apellido = inputApellidos1.getText();
            String tipo = tipoUsuario.getSelection().getActionCommand();

            System.out.print(nombre + apellido + tipo + "-");

            if (nombre == null || apellido == null || tipo == null) {
                JOptionPane.showMessageDialog(null, "Por favor, completa todos los campos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Validar que el tipo es válido
            if (!tipo.equals("Direccion") && !tipo.equals("Secretaria")) {
                JOptionPane.showMessageDialog(null, "Tipo de usuario no válido.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Ejecutar consulta
            ResultSet rs = stmt.executeQuery("SELECT nombre, apellido FROM " + tipo + ";");
            boolean esta = false;

            while (rs.next()) {
                if (rs.getString("nombre").equalsIgnoreCase(nombre.toLowerCase())
                        && rs.getString("apellido").equalsIgnoreCase(apellido.toLowerCase())) {
                    esta = true;
                    usuario = nombre + " " + apellido;
                }
            }

            // Mapear paneles
            Map<String, JPanel> paneles = new HashMap<>();
            paneles.put("Direccion", Direccion);
            paneles.put("Secretaria", Secretaria);

            JPanel panelSeleccionado = paneles.get(tipo);

            if (esta) {
                Inicio.setVisible(false);
                panelSeleccionado.setVisible(true);

            } else {
                JOptionPane.showMessageDialog(null, "Usuario incorrecto", "Error", JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (SQLException ex) {
            Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_consultarUsuario

