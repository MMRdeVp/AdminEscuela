    private String devolverStringPadres(int idpadres) throws SQLException {

        String query2 = "SELECT nombre, apellido, telefono, email FROM padresalumno WHERE id = ?";
        PreparedStatement stmt2 = conexion.prepareStatement(query2);
        stmt2.setInt(1, idpadres);

        ResultSet rs2 = stmt2.executeQuery();

        String rsPadres = "";

        // Verifica si hay resultados en el segundo ResultSet
        if (rs2.next()) {
            rsPadres = rs2.getString("nombre")
                    + " " + rs2.getString("apellido")
                    + " " + rs2.getString("telefono")
                    + " " + rs2.getString("email");
        }

        return rsPadres;
    }

    private String devolverMatriculaAlumno(int idmat) {

        String curso = "";

        try {

            String query = "SELECT nombre FROM curso WHERE id = ?";
            PreparedStatement stmt = conexion.prepareStatement(query);
            stmt.setInt(1, idmat);

            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {
                curso = rs.getString("nombre");
            }
        } catch (SQLException ex) {
            Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

        return curso;
    }
